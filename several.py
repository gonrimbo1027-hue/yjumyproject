# x = 10
x = 10 
# x는 3을 더하거나 같다
x += 3 
# x는 2를 곱하거나 같다
x *= 2 
# x는 4를 나누거나 같다
x /= 4 

# a와 b를 각각 4와 5라고 선언하라
a, b = 4, 5 
# c와 d를 7이라고 선언하라
c = d = 7 
# a와 b를 b와 a라고 선언하라
a, b = b, a 

# "x ="과 x를 출력하라
print("x =", x) 
# "a ="과 a를 출력하라
print("a =", a)
# "b ="과 b를 출력하라
print("b =", b) 
# "c ="과 c를 출력하라
print("c =", c) 
# "d ="과 d를 출력하라
print("d =", d) 

